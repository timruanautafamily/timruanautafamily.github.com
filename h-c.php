                            <p>1. Single Acting Cylinders :
The head end port of these cylinders will operate in a single direction. When the fluid gets pumped into the cylinder barrel, it will extend the piston rod. For generating the return operation(convert to non-pressurized state), a load string or any external force is required. Here, on applying energy, the fluid will drain from barrel to the reservoir. A hydraulic jack is an example of a single acting cylinder. Spring-extend and spring-return are the two types of single acting hydraulic cylinder. The spring-extend, single acting cylinders are used for holding workpieces for a long time.  A hydraulic pressure released brake is an example of this type. The commonly used variety of single acting cylinder is spring-return(material handling applications).</p>
							<p>2. Double Acting Cylinders :
In double acting cylinders, both the head and rod ends contain ports for pumping fluids.  These ports will control the flow of fluid and provide movement in both directions. Pumping hydraulic fluid to the rod end will retract the piston rod and pumping fluid to the head end will extend the piston rod.  Most of the raising and lowering devices are applications of this type. The opening and closing drawers of presses and chippers is a good example of double acting cylinders. Differential and synchronous types are the two categories of double acting cylinders.</p>
							<p>3. Tie-Rod Cylinders : 
Most of the industrial and manufacturing applications use tie-rod cylinders. The advantages of the tie-rod cylinder include ease of maintenance, repair, and assembling.  For holding the end caps of tie rod cylinders, threaded steel rods are used. These end caps will prevent fluid leakages. Depending on the applications, it can use 4 to 20 tie rods.</p>
                            <p>4. Welded Rod Cylinders :
This type of cylinders weld end caps directly to the barrel. So, they are difficult for assembling and disassembling. The compact construction, internal bearing lengths and duty cycle of welded rod cylinders make it suitable for mobile applications.</p>
                            <p>5. Telescopic Cylinders :
This is a single or double acting cylinder. Telescopic cylinder contains more than five tubings nested inside each other. These nested tubings are called stages and the diameter of each nested tube will become lesser.</p>
