                <h4>HYDRAULIC CYLINDER</h4>
                <p>Hydraulic cylinders get their power from pressurized hydraulic fluid, which is typically oil. The hydraulic cylinder consists of a cylinder barrel, in which a piston connected to a piston rod moves back and forth. The barrel is closed on one end by the cylinder bottom (also called the cap) and the other end by the cylinder head (also called the gland) where the piston rod comes out of the cylinder. The piston has sliding rings and seals. The piston divides the inside of the cylinder into two chambers, the bottom chamber (cap end) and the piston rod side chamber (rod end/head-end).</p>
                <p>Hydraulic cylinders are used in earth-moving equipment to lift or lower the boom, arm, or bucket. These cylinders are also used in hydraulic bending machine, metal sheet shearing machine, particle board or plywood making hot press.</p>
